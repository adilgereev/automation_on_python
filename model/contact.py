class Contact:
    def __init__(self, firstname=None,
                 middlename=None,
                 nickname=None,
                 company=None,
                 address=None,
                 phone_num=None,
                 email=None):
        self.firstname = firstname
        self.middlename = middlename
        self.nickname = nickname
        self.company = company
        self.address = address
        self.phone_num = phone_num
        self.email = email
